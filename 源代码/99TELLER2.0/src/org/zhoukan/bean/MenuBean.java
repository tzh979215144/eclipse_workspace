package org.zhoukan.bean;

public class MenuBean {

	private String POWER_ID = null;
	
	private String POWER_NAME = null;
	
	private String MENU_TAG = null;
	
	private String POWER_CONFIG = null;
	
	private String POWER_PATH = null;
	
	private String POWER_FATHER = null;

	public String getPOWER_ID() {
		return POWER_ID;
	}

	public void setPOWER_ID(String pOWERID) {
		POWER_ID = pOWERID;
	}

	public String getPOWER_NAME() {
		return POWER_NAME;
	}

	public void setPOWER_NAME(String pOWERNAME) {
		POWER_NAME = pOWERNAME;
	}

	public String getMENU_TAG() {
		return MENU_TAG;
	}

	public void setMENU_TAG(String mENUTAG) {
		MENU_TAG = mENUTAG;
	}

	public String getPOWER_CONFIG() {
		return POWER_CONFIG;
	}

	public void setPOWER_CONFIG(String pOWERCONFIG) {
		POWER_CONFIG = pOWERCONFIG;
	}

	public String getPOWER_PATH() {
		return POWER_PATH;
	}

	public void setPOWER_PATH(String pOWERPATH) {
		POWER_PATH = pOWERPATH;
	}

	public String getPOWER_FATHER() {
		return POWER_FATHER;
	}

	public void setPOWER_FATHER(String pOWERFATHER) {
		POWER_FATHER = pOWERFATHER;
	}
	
	
}
